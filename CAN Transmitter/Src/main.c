void udelay(uint32_t time){
	uint32_t tick_total_latency,tick_before_latency;
	HAL_TIM_Base_Init(&htim2);
	HAL_TIM_Base_Start(&htim2);
	tick_before_latency = __HAL_TIM_GetCounter(&htim2);
	do{		
		
		tick_total_latency = __HAL_TIM_GetCounter(&htim2) - tick_before_latency;
	}while(tick_total_latency < time);
}
